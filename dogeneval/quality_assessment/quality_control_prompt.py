
PROMPT = """您是一位问题生成质量控制专家。您的任务是根据给定的模板评估生成的问题，并确定它是否符合所需的标准。请分析以下问题组成部分并提供您的评估：

模板：
{template}

生成的问题，其中answer字段为答案：
{question_components}

指示：
1. 仔细检查生成问题的每个组成部分。
2. 将生成的组成部分与模板结构进行比较。
3. 检查以下问题：
   - 信息缺失或不完整
   - 内容错误或事实性错误
   - 组成部分之间的不一致
   - 语法或结构问题
   - 模糊或缺乏清晰度

4. 提供详细的问题评估，解决以下几点：
   a. 问题是否符合给定的模板结构？
   b. 是否所有必需的组成部分都存在且完整？
   c. 内容是否准确并与主题相关？
   d. 问题是否构造良好且清晰？
   e. 这些组成部分是否可以组合成一个连贯且适当的问题？

5. 给出您的最终评估。

请以JSON格式提供您的评估结果，格式如下：

{{
    "评估": {{
        "符合模板结构": true/false,
        "组成部分完整": true/false,
        "内容准确性": true/false,
        "问题构造": true/false,
        "组合适当性": true/false
    }},
    "最终评估": "批准"/"建议修改"/"拒绝",
    "修改建议": "如果需要修改，在这里提供具体建议",
    "拒绝原因": "如果拒绝，在这里解释原因"
}}

示例输出：
{{
    "评估": {{
        "符合模板结构": true,
        "组成部分完整": true,
        "内容准确性": true,
        "问题构造": true,
        "组合适当性": true
    }},
    "最终评估": "批准",
    "修改建议": "",
    "拒绝原因": ""
}}

请根据上述指示提供您的评估结果。
"""

def generate_prompt(template, question):
    return PROMPT.format(template=template, question_components=question)